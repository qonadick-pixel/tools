<script>
    $(document).ready(function(){
        $('#sendcommand').click(function(){
            $.ajax({
                type: 'GET',
                url: '/engine/core/sendcmd.php?type=sendcommand',
                data: $('#formsendcommand').serialize(),
                success: function(data) {
                    if(data.search('#success') != -1) {
                        data = data.replace('#success', ''); 
                        new Notify ({
                            title: 'Успешно',
                            text: data,
                            status: 'success',
                            autoclose: true,
                            autotimeout: 2000                    
                        })
                        setInterval(() => {
                            window.location = "/bot"
                        }, 500);
                        play()
                    } else if (data.search('#error') != -1) {
                        data = data.replace('#error', ''); 
                        new Notify ({
                            title: 'Ошибка',
                            text: data,
                            status: 'error',
                            autoclose: true,
                            autotimeout: 2000                    
                        })

                        play()
                    } else {
                        new Notify ({
                            title: 'Ошибка',
                            text: 'Ошибка на стороне сервера, срочно отпишите администрации!',
                            status: 'warning',
                            autoclose: true,
                            autotimeout: 2000                    
                        })

                        play()
                    }
                }
            });
        });
    });
</script>
